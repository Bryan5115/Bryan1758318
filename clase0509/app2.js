let numero1 = prompt("ingresa un numero");
let numero2 = prompt("ingresa un segundo numero");

//let resultado = numero1+numero2;


//sumna
let resultado= parseInt(numero1)+parseInt(numero2);
console.log("el resultado de la suma es.. " + resultado)

//resta

let resultado1= parseInt(numero1)-parseInt(numero2);
console.log("el resultado de la resta es.. " + resultado1)


//multiplicacion

let resultado2= parseInt(numero1)*parseInt(numero2);
console.log("el resultado de la multi es.. " + resultado2)